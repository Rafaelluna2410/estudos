numA = int(input("digite a \n"))
numB = int(input("digite b \n"))

operation = input("write the operation \n")


if operation == "sum":
    print(numA+numB)
elif operation == "subtraction":
    print (numA - numB)
elif operation == "division":
    print (numA / numB)
elif operation =="multiplication":
    print(numA * numB)